void USB_Received() {
//	uint8_t txID[4];
	uint32_t id = 0;
	uint32_t dlc = 8;
	uint8_t txData[8];
	char buff[4];
	for (int i = 0; i < 4; i++) {
		sprintf(&buff[i], "%d", buffer[i]-'0');
	}
	id = (uint32_t) atoi(buff);

	for (int i = 5; i < dlc + 5; i++) {
		txData[i - 5] = buffer[i];
	}

	FDCAN_Send_Command(id, dlc, txData);
}

